package Aula16.ATP22.Exercicio3;

public class Calculadora {

    public int soma(int n1, int n2) {
        int resultadoInt = n1 + n2;
        return resultadoInt;
    }

    public double soma(double n1, double n2) {
        double resultadoDouble = n1 + n2;
        return resultadoDouble;

    }

    public float soma(float n1, float n2, float n3) {
        float resultadoFloat = n1 + n2 + n3;
        return resultadoFloat;
    }

    public long soma(long n1, long n2, long n3) {
        long resultadoLong = n1 + n2 + n3;
        return resultadoLong;
    }

    }

